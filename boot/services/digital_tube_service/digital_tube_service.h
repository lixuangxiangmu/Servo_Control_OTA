#ifndef DIGITAL_TUBE_SERVICE_H
#define DIGITAL_TUBE_SERVICE_H

#include "display/digital_tube/digital_tube.h"

#include <stdint.h>

/* 数码管业务服务配置，由 application/app_config.c 注入。 */
typedef struct
{
    digital_tube_cfg_t tube_cfg;    /* 底层数码管组件配置。 */
    uint16_t refresh_period_ms;     /* 扫描刷新周期，建议 1ms。 */
    uint8_t auto_start;             /* 初始化后是否自动启动显示刷新。 */
} digital_tube_service_cfg_t;

/*****************************************************************************
@brief: 初始化数码管业务服务
@para:cfg 应用层注入的数码管配置、刷新周期和自动启动选项
@return: RET_OK 表示初始化成功，其他返回值表示失败
@note: 该函数会初始化底层 digital_tube 组件，并创建 FreeRTOS 软件定时器。
*******************************************************************************/
int digital_tube_service_init(const digital_tube_service_cfg_t *cfg);

/*****************************************************************************
@brief: 启动数码管动态扫描刷新
@return: RET_OK 表示启动成功，其他返回值表示失败
@note: 启动后软件定时器会周期调用 digital_tube_service_poll()。
*******************************************************************************/
int digital_tube_service_start(void);

/*****************************************************************************
@brief: 停止数码管动态扫描刷新并熄灭显示
@return: RET_OK 表示停止成功，其他返回值表示失败
*******************************************************************************/
int digital_tube_service_stop(void);

/*****************************************************************************
@brief: 打开或关闭数码管显示
@para:enable 非 0 表示打开显示并启动刷新，0 表示关闭刷新并熄灭显示
@return: RET_OK 表示设置成功，其他返回值表示失败
*******************************************************************************/
int digital_tube_service_set_enable(uint8_t enable);

/*****************************************************************************
@brief: 清空数码管显示缓冲区
@return: RET_OK 表示清空成功，其他返回值表示失败
*******************************************************************************/
int digital_tube_service_clear(void);

/*****************************************************************************
@brief: 设置单个数码管位的显示内容
@para:pos 位索引，0 表示最左侧，DIGITAL_TUBE_DIGIT_COUNT - 1 表示最右侧
@para:num 显示数字，范围 0-9
@para:dp_enable 非 0 表示点亮小数点，0 表示不点亮小数点
@return: RET_OK 表示设置成功，其他返回值表示失败
*******************************************************************************/
int digital_tube_service_set_digit(uint8_t pos, uint8_t num, uint8_t dp_enable);

/*****************************************************************************
@brief: 显示一个无符号整数
@para:number 待显示数字，范围 0-9999，超过 9999 时按 9999 显示
@return: RET_OK 表示设置成功，其他返回值表示失败
*******************************************************************************/
int digital_tube_service_show_number(uint16_t number);

/*****************************************************************************
@brief: 数码管扫描轮询入口
@return: 无
@note: 通常由服务内部的软件定时器调用，应用层一般不需要直接调用。
*******************************************************************************/
void digital_tube_service_poll(void);

#endif

/*****************************************************************************
 * @file    main.c
 * @brief   Bootloader 程序入口
 *
 * Bootloader 裸机运行，不依赖 FreeRTOS。
 * 上电后直接进入 boot_main() 状态机，正常情况下最终会跳转到 APP。
 *****************************************************************************/

#include "app_main.h"

int main(void)
{
    boot_main();

    /* 正常情况下不会执行到这里 (boot_main 最终会跳转 APP 或死循环)。 */
    while (1)
    {
    }
}

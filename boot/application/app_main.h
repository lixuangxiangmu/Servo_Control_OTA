/*****************************************************************************
 * @file    app_main.h
 * @brief   Bootloader 主逻辑模块
 *****************************************************************************/

#ifndef APP_MAIN_H
#define APP_MAIN_H

#include <stdint.h>

/*****************************************************************************
 * @brief:  Bootloader 主入口，初始化硬件并进入主循环状态机
 * @note:   该函数不会返回，正常情况下最终会跳转到 APP。
 *****************************************************************************/
void boot_main(void);

/*****************************************************************************
 * @brief: 检查 APP 向量表是否具备最基本的启动合法性
 * @return: 1 表示 SP/ResetHandler 合法，0 表示不可跳转
 *****************************************************************************/
uint8_t boot_app_is_valid(void);

/*****************************************************************************
 * @brief: 跳转到 APP
 * @note:  跳转成功后不会返回；APP 不合法时直接返回。
 *****************************************************************************/
void boot_jump_to_app(void);

#endif /* APP_MAIN_H */

/*****************************************************************************
 * @file    app_state.h
 * @brief   系统全局状态管理模块头文件
 *          作为系统的状态数据中心，承担以下职责：
 *
 *          1. 参数管理
 *             - 统一管理所有可配置参数（舵机速度、角度限位、按键时间、断电策略等）
 *             - 提供线程安全的读写接口，供控制、显示、HMI 等模块并发访问
 *
 *          2. 运行时状态追踪
 *             - 跟踪系统当前工作模式（正常/锁定/自动校准）
 *             - 记录舵机实时姿态与角度
 *             - 监控电源电压与堵转 ADC 采样值
 *             - 追踪参数加载来源（EEPROM / 默认值 / CRC 错误等）
 *
 *          3. 线程安全保证
 *             - 使用 FreeRTOS 互斥量（Mutex）保护所有状态读写操作
 *             - 多任务并发访问（控制任务、显示任务、HMI 输入）通过统一加锁保证数据一致性
 *
 *          4. 类型定义中心
 *             - 集中定义系统核心枚举：工作模式、舵机姿态、显示模式、解锁模式、存储来源
 *             - 集中定义系统核心结构体：参数结构体、运行时状态结构体
 *             - 避免各模块各自定义导致的类型不一致
 * @author  LXA
 * @version v1.0
 * @date    2026-06-16
 *****************************************************************************/

#ifndef APP_STATE_H
#define APP_STATE_H

#include <stdint.h>

/*===========================================================================
 * 工作模式枚举
 *===========================================================================*/

/**
 * @brief 系统工作模式
 *
 * 定义系统的顶层运行状态，决定控制模块对各输入通道的响应策略。
 * 工作模式切换由 app_control 模块管理。
 */
typedef enum
{
    APP_WORK_MODE_NORMAL = 0,       /**< 正常工作：允许按键、遥控和蓝牙切换舵机状态 */
    APP_WORK_MODE_LOCK,             /**< 自锁模式：舵机回初始角后保持锁定，仅监控解锁条件 */
    APP_WORK_MODE_AUTO_CALIBRATE,   /**< 自动标定：自动寻找折叠角和初始角的机械限位 */
} app_work_mode_t;

/*===========================================================================
 * 舵机姿态枚举
 *===========================================================================*/

/**
 * @brief 舵机姿态（折叠/初始）
 *
 * 标识舵机当前处于折叠状态还是初始（展开）状态，
 * 用于姿态切换逻辑、LED 指示和断电记忆。
 */
typedef enum
{
    APP_SERVO_POSE_INIT = 0,        /**< 初始/展开状态（舵机位于初始角） */
    APP_SERVO_POSE_FOLD,            /**< 折叠状态（舵机位于折叠角） */
} app_servo_pose_t;

/*===========================================================================
 * 显示模式枚举
 *===========================================================================*/

/**
 * @brief 数码管显示/编辑模式
 *
 * 定义数码管当前所处的显示或参数编辑模式。
 * 模式流转路径（由 app_display_cycle_edit_mode 驱动）：
 *   NORMAL -> SET_FOLD -> SET_INIT -> SET_SPEED -> NORMAL
 */
typedef enum
{
    APP_DISPLAY_MODE_NORMAL = 0,    /**< 正常显示当前舵机角度 */
    APP_DISPLAY_MODE_SET_FOLD,      /**< 设置折叠角 */
    APP_DISPLAY_MODE_SET_INIT,      /**< 设置初始角 */
    APP_DISPLAY_MODE_SET_SPEED,     /**< 设置舵机速度 */
} app_display_mode_t;

/*===========================================================================
 * 解锁模式枚举
 *===========================================================================*/

/**
 * @brief 锁定状态下的解锁方式
 *
 * 定义系统在锁定模式下允许的解锁通道。
 * APP_UNLOCK_MODE_ALL 允许按键、遥控器、蓝牙解锁；
 * APP_UNLOCK_MODE_BLE_ONLY 允许全部通道上锁，但仅允许蓝牙解锁。
 */
typedef enum
{
    APP_UNLOCK_MODE_ALL = 0,        /**< 按键、遥控器和蓝牙都允许解锁 */
    APP_UNLOCK_MODE_BLE_ONLY,       /**< 仅蓝牙允许解锁 */
} app_unlock_mode_t;

/*===========================================================================
 * 参数存储来源枚举
 *===========================================================================*/

/**
 * @brief 参数加载来源追踪
 *
 * 记录最近一次参数加载的来源，用于上电诊断和异常处理。
 * 当 EEPROM 读取失败或数据校验不通过时，自动回退到默认参数。
 */
typedef enum
{
    APP_STORAGE_SOURCE_UNKNOWN = 0,         /**< 尚未完成参数加载 */
    APP_STORAGE_SOURCE_EEPROM,              /**< 参数来自 EEPROM，CRC 和范围均有效 */
    APP_STORAGE_SOURCE_DEFAULT_IO,          /**< EEPROM 读取失败（IO 错误），使用默认参数 */
    APP_STORAGE_SOURCE_DEFAULT_CRC,         /**< CRC 校验无效，使用默认参数 */
    APP_STORAGE_SOURCE_DEFAULT_RANGE,       /**< 参数范围非法，使用默认参数 */
} app_storage_source_t;

/*===========================================================================
 * 系统参数结构体
 *===========================================================================*/

/**
 * @brief 系统可配置参数集
 *
 * 包含所有可通过 HMI（按键/编码器/遥控器/蓝牙）配置并持久化到 EEPROM 的参数。
 * 每个字段均有值域约束（定义于 app_storage.h），写入前须通过 app_storage_params_is_valid 校验。
 */
typedef struct
{
    uint8_t servo_speed;                    /**< 舵机速度，范围 0~5，数值越大速度越快 */
    uint8_t angle_fold;                     /**< 折叠角，范围 60~179（度） */
    uint8_t angle_init;                     /**< 初始角，范围 0~60（度） */
    uint8_t enter_lock_sec;                 /**< 长按进入自锁的时间，范围 1~5（秒） */
    uint8_t enter_unlock_sec;               /**< 超长按退出自锁的时间，范围 5~20（秒） */
    uint8_t power_off_return;               /**< 断电是否回位：0=回位到初始角，1=保持当前位置断电 */
    uint8_t power_off_fold_state;           /**< 掉电前舵机状态：0=初始态，1=折叠态（供记忆功能恢复） */
    uint8_t memory_function_enable;         /**< 掉电记忆功能：0=关闭，1=开启（上电恢复上次姿态） */
    uint8_t unlock_mode;                    /**< 锁定后解锁模式，见 app_unlock_mode_t */
    uint16_t remote_key_code;               /**< 已配对遥控器特征码（data[0:1]，0 表示未配对） */
} app_servo_params_t;

/*===========================================================================
 * 运行时状态结构体
 *===========================================================================*/

/**
 * @brief 系统运行时状态
 *
 * 记录系统运行过程中的实时状态信息，由各模块通过 app_state_set_xxx 接口更新，
 * 供显示、控制、检测等模块通过 app_state_get_runtime 查询。
 * 所有字段通过互斥量保护，保证多任务并发访问的数据一致性。
 */
typedef struct
{
    app_work_mode_t work_mode;              /**< 当前产品工作模式 */
    app_servo_pose_t servo_pose;            /**< 当前舵机姿态（折叠/初始） */
    app_display_mode_t display_mode;        /**< 当前显示/编辑模式 */
    app_storage_source_t storage_source;    /**< 最近一次参数加载来源 */
    uint8_t current_angle;                  /**< 当前应用层认为的舵机角度（0~179°） */
    uint8_t display_timeout_active;         /**< 设置界面超时计数是否运行（1=运行中） */
    uint32_t power_voltage_mv;              /**< 主电源电压，单位 mV */
    uint32_t locked_rotor_raw;              /**< 堵转检测 ADC 原始采样值 */
} app_servo_runtime_state_t;

/*===========================================================================
 * 对外接口
 *===========================================================================*/

/*****************************************************************************
 * @brief:  状态管理模块初始化
 *          按顺序完成：
 *            1. 幂等性检查（s_app_state_inited 标志）
 *            2. 创建互斥量（保护参数和运行时状态的并发访问）
 *            3. 填充运行时状态默认值（正常模式、初始姿态、未知存储来源）
 *            4. 标记 s_app_state_inited = 1
 *          应在 FreeRTOS 内核启动后、其他应用模块初始化前调用。
 * @para:   无
 * @return: RET_OK            初始化成功（包括已初始化后再次调用）
 *          RET_NO_MEMORY      互斥量创建失败（内存不足）
 *****************************************************************************/
int app_state_init(void);

/*****************************************************************************
 * @brief:  写入系统参数（整体替换）
 *          将参数结构体整体拷贝到内部缓存，加锁保证原子性。
 * @para:   params  指向源参数结构体（const，函数内部拷贝，调用方可释放）
 * @return: RET_OK            写入成功
 *          RET_INVALID_PARAM  params 为空指针
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_set_servo_params(const app_servo_params_t *params);

/*****************************************************************************
 * @brief:  读取系统参数（整体拷贝）
 *          将内部缓存的参数结构体整体拷贝到调用方提供的缓冲区。
 * @para:   params  指向目标参数结构体（由调用方分配内存）
 * @return: RET_OK            读取成功
 *          RET_INVALID_PARAM  params 为空指针
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_get_servo_params(app_servo_params_t *params);

/*****************************************************************************
 * @brief:  写入运行时状态（整体替换）
 *          将运行时状态结构体整体拷贝到内部缓存。
 * @para:   runtime  指向源运行时状态结构体
 * @return: RET_OK            写入成功
 *          RET_INVALID_PARAM  runtime 为空指针
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_set_runtime(const app_servo_runtime_state_t *runtime);

/*****************************************************************************
 * @brief:  读取运行时状态（整体拷贝）
 *          将内部缓存的运行时状态结构体整体拷贝到调用方提供的缓冲区。
 * @para:   runtime  指向目标运行时状态结构体（由调用方分配内存）
 * @return: RET_OK            读取成功
 *          RET_INVALID_PARAM  runtime 为空指针
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_get_runtime(app_servo_runtime_state_t *runtime);

/*****************************************************************************
 * @brief:  设置工作模式（单字段更新）
 *          线程安全地更新运行时状态中的 work_mode 字段。
 *          相比整体替换，单字段更新粒度更细，减少锁持有时间。
 * @para:   mode  目标工作模式
 * @return: RET_OK            设置成功
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_set_work_mode(app_work_mode_t mode);

/*****************************************************************************
 * @brief:  设置舵机姿态和当前角度（双字段原子更新）
 *          线程安全地同时更新 servo_pose 和 target_angle
 *          保证姿态和角度的一致性（不会出现姿态已更新但角度未更新的中间状态）。
 * @para:   pose          目标舵机姿态
 * @para:   target_angle  目标舵机角度（0~180°）
 * @return: RET_OK            设置成功
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_set_servo_pose(app_servo_pose_t pose, uint8_t current_angle);

/*****************************************************************************
 * @brief:  设置显示模式（单字段更新）
 *          线程安全地更新运行时状态中的 display_mode 字段。
 * @para:   mode  目标显示模式
 * @return: RET_OK            设置成功
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_set_display_mode(app_display_mode_t mode);

/*****************************************************************************
 * @brief:  设置参数存储来源（单字段更新）
 *          记录最近一次参数加载的来源，用于上电诊断。
 * @para:   source  存储来源
 * @return: RET_OK            设置成功
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_set_storage_source(app_storage_source_t source);

/*****************************************************************************
 * @brief:  设置电源电压和堵转 ADC 采样值（双字段原子更新）
 *          线程安全地同时更新 power_voltage_mv 和 locked_rotor_raw，
 *          由控制模块中的电源检测和堵转检测任务周期性调用。
 * @para:   power_voltage_mv   主电源电压（mV）
 * @para:   locked_rotor_raw   堵转检测 ADC 原始值
 * @return: RET_OK            设置成功
 *          其他                加锁失败时返回对应错误码
 *****************************************************************************/
int app_state_set_voltage(uint32_t power_voltage_mv, uint32_t locked_rotor_raw);

#endif /* APP_STATE_H */

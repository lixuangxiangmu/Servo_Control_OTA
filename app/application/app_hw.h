/*****************************************************************************
 * @file    app_hw.h
 * @brief   硬件抽象层（HAL）头文件
 *          提供统一的硬件操作接口，向上层应用模块屏蔽底层器件驱动的具体实现。
 *          目前封装的功能包括：
 *            - 电源保持（Power Hold）GPIO 控制
 *            - 供电电压 ADC 采样与电压值换算
 *            - 堵转电流 ADC 采样
 *            - 蓝牙透传串口（UART3）的读写操作
 *          所有接口返回统一错误码（return_code.h 中定义），
 *          调用方无需关心底层 dev_gpio / dev_adc / dev_uart 的具体实现。
 * @author  GD32 Servo Control Team
 * @version v1.0
 * @date    2026-06-17
 *****************************************************************************/

#ifndef APP_HW_H
#define APP_HW_H

#include <stdint.h>

/*===========================================================================
 * 电源管理接口
 *===========================================================================*/

/*****************************************************************************
 * @brief:  设置电源保持（Power Hold）引脚电平
 *          控制系统的自锁供电电路：
 *            - enable != 0：GPIO 输出高电平，保持供电（系统持续运行）
 *            - enable == 0：GPIO 输出低电平，断开自锁（系统断电）
 *          典型使用场景：
 *            1. 系统启动后立即拉高（app_control_init 中调用），防止启动阶段意外断电
 *            2. 低电压断电时拉低（app_control_check_power_off 中调用），关闭系统
 *            3. 锁定/解锁流程中的电源策略控制
 * @para:   enable  1 = 开启电源保持，0 = 关闭电源保持
 * @return: RET_OK         操作成功
 *          其他            底层 GPIO 驱动返回的错误码
 *****************************************************************************/
int app_hw_power_hold_set(uint8_t enable);

/*****************************************************************************
 * @brief:  读取供电电压值（毫伏）
 *          通过 ADC 采样电源分压后的电压值，经硬件换算公式转换为毫伏。
 *          换算公式（沿用老工程）：
 *            mV = (ADC原始值 × 36300 / 4095) - 200
 *          其中 36300 为分压系数 × 参考电压的乘积，-200mV 为补偿修正值。
 *          当计算结果小于 200mV 时返回 0（防止无意义的下溢值）。
 * @para:   voltage_mv  [出参] 指向存放电压值（毫伏）的变量指针，不可为 NULL
 * @return: RET_OK            读取成功，voltage_mv 已更新
 *          RET_INVALID_PARAM  voltage_mv 为 NULL（参数无效）
 *          其他               底层 ADC 驱动返回的错误码
 *****************************************************************************/
int app_hw_read_power_voltage_mv(uint32_t *voltage_mv);

/*===========================================================================
 * 堵转检测接口
 *===========================================================================*/

/*****************************************************************************
 * @brief:  读取堵转电流 ADC 原始采样值
 *          通过 ADC 采样舵机供电回路中的检流电阻电压，反映舵机实时电流大小。
 *          原始值未经任何换算，由上层（app_control.c）根据阈值自行判断堵转。
 *          用途：
 *            - 堵转保护任务（RotorCheck）中实时监测舵机电流
 *            - 自动校准流程中检测机械限位（舵机运动到末端时电流急剧上升）
 * @para:   raw  [出参] 指向存放 ADC 原始值的变量指针，不可为 NULL
 * @return: RET_OK            读取成功，raw 已更新
 *          RET_INVALID_PARAM  raw 为 NULL（参数无效）
 *          其他               底层 ADC 驱动返回的错误码
 *****************************************************************************/
int app_hw_read_locked_rotor_raw(uint32_t *raw);

/*===========================================================================
 * 蓝牙通信接口
 *===========================================================================*/

/*****************************************************************************
 * @brief:  从蓝牙串口（UART3）读取一个字节
 *          阻塞式读取，在 timeout_ticks 超时周期内等待数据到达。
 *          若超时未收到数据，返回超时错误码。
 *          适用于蓝牙模块的单字节命令/响应交互场景。
 * @para:   buf           [出参] 存放读取到的字节的缓冲区指针
 * @para:   timeout_ticks 超时等待的 FreeRTOS 滴答数
 *                        （通常使用 pdMS_TO_TICKS(ms) 转换毫秒值）
 * @return: RET_OK        读取成功
 *          RET_TIMEOUT   超时未收到数据
 *          其他           底层 UART 驱动返回的错误码
 *****************************************************************************/
int app_hw_bluetooth_read(uint8_t *buf, uint32_t timeout_ticks);

/*****************************************************************************
 * @brief:  向蓝牙串口（UART3）写入指定长度的数据
 *          阻塞式写入，在 timeout_ticks 超时周期内等待发送完成。
 *          适用于发送二进制协议帧或原始字节数据。
 * @para:   buf           待发送数据的缓冲区指针
 * @para:   len           待发送数据的字节长度
 * @para:   timeout_ticks 发送超时的 FreeRTOS 滴答数
 * @return: RET_OK        发送成功
 *          RET_TIMEOUT   发送超时（对端未及时取走数据或硬件忙）
 *          其他           底层 UART 驱动返回的错误码
 *****************************************************************************/
int app_hw_bluetooth_write(const uint8_t *buf, uint32_t len, uint32_t timeout_ticks);

/*****************************************************************************
 * @brief:  向蓝牙串口发送一个以 '\0' 结尾的文本字符串
 *          内部自动计算字符串长度并调用 app_hw_bluetooth_write() 发送，
 *          超时时间使用默认值 DEV_UART_DEFAULT_TIMEOUT_TICKS。
 *          适用于发送 AT 指令或 JSON 文本等字符串场景，
 *          调用方无需手动计算长度，简化上层代码。
 * @para:   text  '\0' 结尾的字符串指针，不可为 NULL
 * @return: RET_OK            发送成功
 *          RET_INVALID_PARAM  text 为 NULL（参数无效）
 *          其他                底层发送函数的错误码
 *****************************************************************************/
int app_hw_bluetooth_write_text(const char *text);

#endif /* APP_HW_H */
